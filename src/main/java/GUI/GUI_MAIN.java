    //Inicializes the board
    private void InitializeBoard() {
        for (int i = 0; i < 8; i++) {
            for (int j = 0; j < 8; j++) {

                JLabel label = getLabel(numberToString(i) + String.valueOf(j + 1));
                if (game.getBoard()[i][j].containsPiece()) {
                    ImageIcon icon = new ImageIcon(game.getBoard()[i][j].getPiece().icon);
                    label.setIcon(icon);
                } else {
                    label.setIcon(null);
                }
            }
        }
    }

    //Moves the selected piece or selects a new one or deselects the piece
    private void moveOrSelect(String position) {
        if (gameisover) {
            return;
        }
        ArrayList<String> possibleMoves;
        //Checks if there is no selected square and if there is a piece to select
        if (this.selected == null && game.getSquareAt(position).containsPiece()) {
            //Checks if the player trying to select the 
            if (this.Black && game.getSquareAt(position).getPiece().Black) {
                this.selected = game.getSquareAt(position);
                showPossibleMoves(position);
            } else if (!this.Black && !game.getSquareAt(position).getPiece().Black) {
                this.selected = game.getSquareAt(position);
                showPossibleMoves(position);
            }

        } else if (this.selected != null) {
            possibleMoves = game.possibleMoves(this.selected.getPosition());
            //Checks if the piece can go to the selected position
            if (possibleMoves.contains(position)) {

                if (game.getSquareAt(position).getPiece() instanceof King) {
                    gameisover = !gameisover;
                }

                //Checks if there is a piece on that position
                if (!game.getSquareAt(position).containsPiece()) {

                    //Adds the play to the board
                    addPlay(this.selected.getPosition(), position);

                    //Removes the icon from the actual square
                    getLabel(this.selected.getPosition()).setIcon(null);

                    //Moves the piece to the new position
                    game.movePiece(this.selected.getPosition(), position);

                    //Changes the new position icon to the piece icon
                    ImageIcon icon = new ImageIcon(this.selected.getPiece().getIcon());
                    getLabel(position).setIcon(icon);

                    //In case there is a piece in there it eats the piece
                } else if (game.getSquareAt(position).getPiece().Black != this.selected.getPiece().Black) {

                    //Adds the play to the board
                    addPlay(this.selected.getPosition(), position);

                    //Remove i icone do bloco atual
                    getLabel(this.selected.getPosition()).setIcon(null);

                    //Move a pea para a nova posio
                    game.movePiece(this.selected.getPosition(), position);

                    //Altera o icone da nova posio para o icone da pea
                    ImageIcon icon = new ImageIcon(this.selected.getPiece().getIcon());
                    getLabel(position).setIcon(icon);
                }

                //Advances to the next player
                this.Black = !Black;
            }
            stopShowPossibleMoves(possibleMoves);
            //Deselects the piece
            this.selected = null;

            if (gameisover) {
                whoIsPlaying(true);
                BOARDTEXT.setText(BOARDTEXT.getText() + "GAME IS OVER");
            } else {
                whoIsPlaying(false);
            }
        }
    }

    //Shows graphically possible moves in the board
    private void showPossibleMoves(String position) {
        ArrayList<String> possibleMoves = game.possibleMoves(position);

        for (int i = 0; i < possibleMoves.size(); i++) {
            ImageIcon icon = new ImageIcon("./src/main/java/Utils/Pointer.png");
            getLabel(possibleMoves.get(i)).setIcon(icon);
        }
    }

    //Stops showing the possible positions to move
    private void stopShowPossibleMoves(ArrayList<String> possibleMoves) {

        ImageIcon icon = new ImageIcon("./src/main/java/Utils/Pointer.png");

        for (String position : possibleMoves) {
            JLabel label = getLabel(position);

            if (!game.getSquareAt(position).containsPiece()) {
                label.setIcon(null);
            } else {
                ImageIcon icon2 = new ImageIcon(game.getSquareAt(position).getPiece().getIcon());
                label.setIcon(icon2);
            }
        }
    }

    //Changes Player UI
    private void whoIsPlaying(boolean win) {
        if (win) {
            if (this.Black) {
                Player2P.setText("WINNER");
                player1P.setText("LOSER");
                Player2Banner.setBackground(Color.orange);
            } else {
                Player2P.setText("LOSER");
                player1P.setText("WINNER");
                Player1Banner.setBackground(Color.orange);
            }
        } else {
            Border borderTurn = BorderFactory.createLineBorder(Color.yellow, 2);
            Border border = BorderFactory.createLineBorder(Color.WHITE, 2);

            if (this.Black) {
                Player1Banner.setBorder(borderTurn);
                Player2P.setText("");
                Player2Banner.setBorder(border);
                player1P.setText("TURN");
            } else {
                Player2P.setText("TURN");
                Player1Banner.setBorder(border);
                Player2Banner.setBorder(borderTurn);
                player1P.setText("");
            }
        }

    }

    //Adds a play to the board
    private void addPlay(String Previous, String next) {
        String player = "";
        if (this.Black) {
            player = game.getPlayer1().getName();
        } else {
            player = game.getPlayer2().getName();
        }

        String Piece = "";

        if (this.selected.getPiece() instanceof King) {
            Piece = "KING";
        } else if (this.selected.getPiece() instanceof Pawn) {
            Piece = "PAWN";
        } else if (this.selected.getPiece() instanceof Bishop) {
            Piece = "BISHOP";
        } else if (this.selected.getPiece() instanceof Knight) {
            Piece = "KNIGHT";
        } else if (this.selected.getPiece() instanceof Queen) {
            Piece = "QUEEN";
        } else {
            Piece = "TOWER";
        }

        BOARDTEXT.setText(BOARDTEXT.getText() + player + "'s " + Piece + " | " + Previous + " -> " + next + "\n");
    }

    //Returns a String for the respective number
    private String numberToString(int i) {
        String char1 = "";
        switch (i) {
            case 0:
                char1 = "A";
                break;
            case 1:
                char1 = "B";
                break;
            case 2:
                char1 = "C";
                break;
            case 3:
                char1 = "D";
                break;
            case 4:
                char1 = "E";
                break;
            case 5:
                char1 = "F";
                break;
            case 6:
                char1 = "G";
                break;
            case 7:
                char1 = "H";
                break;
            default:
                throw new AssertionError();
        }
        return char1;
    }

